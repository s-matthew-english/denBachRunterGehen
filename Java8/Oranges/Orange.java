public class Orange {

  String colour;
  String name;
  Integer weight;

  Orange(String colour, String name, Integer weight) {
    this.colour = colour;
    this.name = name;
    this.weight = weight;
  }

  String getColour() {
    return colour;
  }

  Integer getWeight() {
    return weight;
  }

  String getName() {
    return name;
  }
}
